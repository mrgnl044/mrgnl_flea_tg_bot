    await message.answer(
        f"üëå –û—Ç–ª–∏—á–Ω—ã–π –∫–∞–¥—Ä! –§–æ—Ç–æ {len(photos)}/3 —Å–æ—Ö—Ä–∞–Ω–µ–Ω–æ. –ü—Ä–æ–¥–æ–ª–∂–∞–π—Ç–µ –∏–ª–∏ –Ω–∞–∂–º–∏—Ç–µ '–î–≤–∏–≥–∞–µ–º—Å—è –¥–∞–ª—å—à–µ', –µ—Å–ª–∏ –∑–∞–≤–µ—Ä—à–∏–ª–∏ —Å—ä—ë–º–∫—É.", 
        reply_markup=get_photos_done_keyboard()
    )

